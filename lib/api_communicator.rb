require 'rest-client'
require 'json'
require 'pry'

def get_character_movies_from_api(character_name)
  #make the web request
  response_string = RestClient.get('http://www.swapi.co/api/people/')
  response_hash = JSON.parse(response_string)
  character_array = response_hash["results"]
  found_character = character_array.find do |character|
    character["name"].downcase == character_name.downcase
  end
  movie_name_array = []
  found_character["films"].each do |url|
    res = RestClient.get(url)
    film_hash = JSON.parse(res)
    #movie_name_array.push(film_hash)
    movie_name_array << film_hash
    #binding.pry
  end
  return movie_name_array
  #movie_name_array.push(film_hash["title"])
  # iterate over the response hash to find the collection of `films` for the given
  #   `character`
  # collect those film API urls, make a web request to each URL to get the info
  #  for that film
  # return value of this method should be collection of info about each film.
  #  i.e. an array of hashes in which each hash reps a given film
  # this collection will be the argument given to `print_movies`
  #  and that method will do some nice presentation stuff like puts out a list
  #  of movies by title. Have a play around with the puts with other info about a given film.
end

def print_movies(films)
  # some iteration magic and puts out the movies in a nice list
  films.each do |movie_array|
    puts movie_array["title"]
  end
end

def show_character_movies(character)
  films = get_character_movies_from_api(character)
  print_movies(films)
end

